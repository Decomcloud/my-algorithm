    class Solution {
        public int ladderLength(String beginWord, String endWord, List<String> wordList) {
            Set<String> visited = new HashSet<>();
            Queue<String> queue = new LinkedList<>();
            queue.offer(beginWord);
            visited.add(beginWord);

            // lengthä»£è¡¨ä¸‹ä¸€å±‚çš„æ·±åº¦
            int length = 1;
            // å¦‚æœå¯ä»¥ç»§ç»­è¢«è®¿é—®, é‚£ä¹ˆå°±å˜æˆäº†ç¯
            while (!queue.isEmpty()) {
                length++;
                int size = queue.size();
                for (int i = 0; i < size; i++) {
                    String word = queue.poll();
                    for (String nextWord : getNextWords(word, wordList)) {
                        if (visited.contains(nextWord)) {
                            continue;
                        }

                        if (nextWord.equals(endWord)) {
                            return length;
                        }

                        visited.add(nextWord);
                        queue.offer(nextWord);
                    }
                }
            }
            return 0;
        }

        // å½“è¯çŸ­çš„æ—¶å€™, è¿™ä¸ªæ›´å¿«
        // å¦‚æœæ¯ä¸ªå•ç‹¬æ¯”è¾ƒ, é‚£ä¹ˆè¯è¶Šå¤š, é€Ÿåº¦è¶Šæ…¢
        private List<String> getNextWords(String word, List<String> wordList) {
            List<String> nextWords = new ArrayList<>();
            for (char c = 'a'; c <= 'z'; c++) {
                for (int i = 0; i < word.length(); i++) {
                    if (c == word.charAt(i)) {
                        continue;
                    }
                    String nextWord = replace(word, i, c);
                    if (wordList.contains(nextWord)) {
                        nextWords.add(nextWord);
                    }
                }
            }
            return nextWords;
        }

        private String replace(String word, int index, char c) {
            char[] chars = word.toCharArray();
            chars[index] = c;
            return new String(chars);
        }
    }
